random.seed(42)

def augment(urls, n_extra=50):
    """Lightly vary existing URLs to grow dataset."""
    tlds_bad = ['xyz','top','tk','ml','cc','pw','online','live','biz']
    out = []
    for _ in range(n_extra):
        base = random.choice(urls)
        suffix = f"?ref={random.randint(1000,9999)}&id={random.randint(10000,99999)}"
        out.append(base + suffix)
    return out

benign_extra   = [u + f"?page={random.randint(1,50)}" for u in random.choices(BENIGN, k=50)]
malicious_extra = augment(MALICIOUS, 50)

all_benign   = BENIGN   + benign_extra
all_malicious = MALICIOUS + malicious_extra

rows = (
    [(u, 0) for u in all_benign] +
    [(u, 1) for u in all_malicious]
)
random.shuffle(rows)

